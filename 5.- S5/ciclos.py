# Ciclos
# Ejecución de instrucciones repetitivas hasta que se cumpla una condición, o se finalice el flujo loop

# Ciclo for
# for i in data:
#    intrucciones a relizar

frutas = ["Banana", "Mango", "Apple", "Pear"]

for i in frutas:
    print(i)

# for i in range(len(data))
for i in range(1,6):
    print(i)

# conocemos los elemntos
for i in range(len(frutas)):
    print(i)

